package com.rapiddeploy;

import android.os.Bundle;

import com.google.android.material.floatingactionbutton.FloatingActionButton;
import com.google.android.material.snackbar.Snackbar;
import com.transistorsoft.locationmanager.adapter.BackgroundGeolocation;
import com.transistorsoft.locationmanager.adapter.TSConfig;
import com.transistorsoft.locationmanager.adapter.callback.TSCallback;
import com.transistorsoft.locationmanager.logger.TSLog;
import com.transistorsoft.tsfirebaseproxy.TSFirebaseProxy;

import androidx.appcompat.app.AppCompatActivity;
import androidx.appcompat.widget.Toolbar;

import android.view.View;
import android.view.Menu;
import android.view.MenuItem;

public class MainActivity extends AppCompatActivity {

    @Override
    protected void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);
        setContentView(R.layout.activity_main);
        Toolbar toolbar = findViewById(R.id.toolbar);
        setSupportActionBar(toolbar);

        FloatingActionButton fab = findViewById(R.id.fab);
        fab.setOnClickListener(new View.OnClickListener() {
            @Override
            public void onClick(View view) {
                Snackbar.make(view, "Replace with your own action", Snackbar.LENGTH_LONG)
                        .setAction("Action", null).show();
            }
        });

        TSFirebaseProxy proxy = TSFirebaseProxy.getInstance(getApplicationContext());
        proxy.register(getApplicationContext());

        TSConfig config = TSConfig.getInstance(getApplicationContext());
        config.updateWithBuilder()
                .setDebug(true)
                .setLogLevel(5)
                .setStopOnTerminate(false)
                .commit();

        final BackgroundGeolocation bgGeo = BackgroundGeolocation.getInstance(getApplicationContext());
        bgGeo.ready(new TSCallback() {
            @Override
            public void onSuccess() {
                TSLog.logger.debug("[ready] successe");
                bgGeo.start();
            }

            @Override
            public void onFailure(String s) {
                TSLog.logger.debug("[ready] FAILURE: " + s);
            }
        });
    }

    @Override
    public boolean onCreateOptionsMenu(Menu menu) {
        // Inflate the menu; this adds items to the action bar if it is present.
        getMenuInflater().inflate(R.menu.menu_main, menu);
        return true;
    }

    @Override
    public boolean onOptionsItemSelected(MenuItem item) {
        // Handle action bar item clicks here. The action bar will
        // automatically handle clicks on the Home/Up button, so long
        // as you specify a parent activity in AndroidManifest.xml.
        int id = item.getItemId();

        //noinspection SimplifiableIfStatement
        if (id == R.id.action_settings) {
            return true;
        }

        return super.onOptionsItemSelected(item);
    }
}
